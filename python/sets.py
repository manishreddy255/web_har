# sets is unordered collection of unique elements
# create an empty set

s = set()

# add elements to set
s.add(1)
s.add(2)
s.add(3)
s.add(4)

# print the set
print(s)

# remove 4  from set
s.remove(2)

print(s)

# no elements ever appear twice in a set

# how many elemnts are in the set
print(f"there are {len(s)} in set")
